    private void ordenarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ordenarBotonActionPerformed
        if(!directorio.getContactos().isEmpty()){
            directorio.ordenar();
            actualizarJLIst();
        }
    }//GEN-LAST:event_ordenarBotonActionPerformed

    
    
    private void agregarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregarBotonActionPerformed
        if(!nombreText.getText().isEmpty() && !telefonoText.getText().isEmpty() && !direccionText.getText().isEmpty()
                && !emailText.getText().isEmpty()){
            directorio.agregarContacto(nombreText.getText(),telefonoText.getText(),direccionText.getText(),
         emailText.getText());
         nombreText.setText("");
         telefonoText.setText("");
         direccionText.setText("");
         emailText.setText("");
         modeloLista.addElement(directorio.getContactos().getLast());
         JOptionPane.showMessageDialog(this, "Se agrego el contacto a la lista");
        }
        else{
            JOptionPane.showMessageDialog(this, "Campos vacios!");
        }
    }//GEN-LAST:event_agregarBotonActionPerformed

    private void actualizarJLIst(){
        modeloLista.clear();
            for (Contacto c : directorio.getContactos()) {
                modeloLista.addElement(c);
            }
    }
    
    private void eliminarbotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarbotonActionPerformed
        if(!lista.isSelectionEmpty()){
            int si = JOptionPane.showConfirmDialog(this, "Â¿Seguro deseas eliminar el contacto seleccionado?");
            if(si == 0){
            int pos = lista.getSelectedIndex();
            directorio.eliminarContacto(pos);
            actualizarJLIst();
            }
        }
    }//GEN-LAST:event_eliminarbotonActionPerformed

    private void verSeleccionBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_verSeleccionBotonActionPerformed
        if(!lista.isSelectionEmpty()){
           int pos = lista.getSelectedIndex();
           String mostrar = directorio.verContacto(pos);
           JOptionPane.showMessageDialog(this, mostrar);
        }
    }//GEN-LAST:event_verSeleccionBotonActionPerformed

    private void soloLetras(java.awt.event.KeyEvent evt){
    
        char presion = evt.getKeyChar();
        if(Character.isDigit(presion)){
            getToolkit().beep();
            JOptionPane.showMessageDialog(this, "Solo letras!");
            evt.consume();
            
        }
    }
    
    private void soloNumeros(java.awt.event.KeyEvent evt){
    
        char presion = evt.getKeyChar();
        if(Character.isLetter(presion)){
            getToolkit().beep();
            JOptionPane.showMessageDialog(this, "Solo Numeros!");
            evt.consume();
            
        }
    }
    
    private void buscarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarBotonActionPerformed
        if(!buscarText.getText().isEmpty()){
        
            Contacto c = directorio.buscarContacto(buscarText.getText());
            if(c!=null){
                JOptionPane.showMessageDialog(this, c.toString2());
            }else
                JOptionPane.showMessageDialog(this, "No se encontro el contacto :(");
        }else
            JOptionPane.showMessageDialog(this, "Campos vacios!");
        buscarText.setText("");
    }//GEN-LAST:event_buscarBotonActionPerformed

